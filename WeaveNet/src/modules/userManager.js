/**
 * UserManager - Модуль управления пользователями на гексагональной карте
 * [ЧТО] Создаёт, хранит и обновляет пользователей которые перемещаются по гексам
 * [ЗАЧЕМ] Симуляция активности на карте, пользователи могут двигаться между гексами
 * [PLAN] Добавить типы пользователей, цели, взаимодействие с постройками
 * 
 * [PLAN] Каждый гекс может содержать до 6 пользователей
 * [PLAN] Максимум пользователей = 1.5 * количество гексов
 * [PLAN] Каждую секунду пользователи могут случайно переместиться на соседний гекс или остаться
 */

class UserManager {
    /**
     * Конструктор менеджера пользователей
     * @param {HexGrid} hexGrid - Объект гексагональной сетки
     */
    constructor(hexGrid) {
        // [ЧТО] Ссылка на гексагональную сетку
        // [ЗАЧЕМ] Нужна для доступа к гексам и их связям
        // [PLAN] Использовать для проверки валидности перемещений
        this.hexGrid = hexGrid;
        
        // [ЧТО] Массив всех пользователей
        // [ЗАЧЕМ] Хранение состояния всех юнитов
        // [PLAN] Добавить пространственное разбиение для оптимизации
        this.users = [];
        
        // [ЧТО] Максимальное количество пользователей (1.5 * число гексов)
        // [ЗАЧЕМ] Ограничение для баланса производительности и геймплея
        // [PLAN] Настроить через конфиг
        this.maxUsers = Math.floor(hexGrid.getAllHexes().length * 1.5);
        
        // [ЧТО] Максимум пользователей на один гекс
        // [ЗАЧЕМ] Ограничение заполненности каждого гекса
        // [PLAN] Сделать настраиваемым параметром
        this.maxUsersPerHex = 6;
        
        // [ЧТО] Таймер для периодического обновления
        // [ЗАЧЕМ] Отслеживание интервала в 1 секунду
        // [PLAN] Использовать игровой цикл вместо setTimeout
        this.lastUpdateTime = Date.now();
        this.updateInterval = 1000; // 1 секунда
        
        // [ЧТО] ID для генерации уникальных идентификаторов
        // [ЗАЧЕМ] Каждый пользователь должен иметь уникальный ID
        // [PLAN] Использовать UUID в будущем
        this.nextUserId = 0;
        
        console.log(`[UserManager] Инициализирован. Макс пользователей: ${this.maxUsers}, на гекс: ${this.maxUsersPerHex}`);
    }
    
    /**
     * Создание нового пользователя
     * [ЧТО] Создаёт объект пользователя со случайной позицией
     * [ЗАЧЕМ] Добавление новых пользователей на карту
     * [PLAN] Добавить разные типы пользователей
     * @returns {Object|null} Объект пользователя или null если достигнут лимит
     */
    createUser() {
        // [ЧТО] Проверяем не достигнут ли максимальный лимит
        // [ЗАЧЕМ] Защита от переполнения
        // [PLAN] Добавить очередь ожидания если лимит достигнут
        if (this.users.length >= this.maxUsers) {
            console.log('[UserManager] Достигнут максимальный лимит пользователей');
            return null;
        }
        
        // [ЧТО] Находим случайный гекс где есть место
        // [ЗАЧЕМ] Пользователь должен появиться на гексе где < 6 пользователей
        // [PLAN] Умный выбор гекса (ближе к центру, равномерно и т.д.)
        const availableHexes = this.getAvailableHexes();
        
        if (availableHexes.length === 0) {
            console.log('[UserManager] Нет доступных гексов для нового пользователя');
            return null;
        }
        
        // [ЧТО] Выбираем случайный гекс из доступных
        // [ЗАЧЕМ] Случайное распределение пользователей
        // [PLAN] Взвешенный выбор для лучшего распределения
        const randomHex = availableHexes[Math.floor(Math.random() * availableHexes.length)];
        
        // [ЧТО] Создаём объект пользователя
        // [ЗАЧЕМ] Основная структура данных для пользователя
        // [PLAN] Добавить больше свойств (тип, состояние, цели)
        const user = {
            id: this.nextUserId++,
            hexId: randomHex.id,
            createdAt: Date.now(),
            color: this.generateRandomColor(),
            moveCooldown: 0 // Задержка перед следующим перемещением
        };
        
        // [ЧТО] Добавляем пользователя в массив
        // [ЗАЧЕМ] Регистрация нового пользователя
        // [PLAN] Оптимизировать добавление для больших массивов
        this.users.push(user);
        
        return user;
    }
    
    /**
     * Получение списка гексов где есть место для новых пользователей
     * [ЧТО] Фильтрует гексы где < 6 пользователей
     * [ЗАЧЕМ] Для размещения новых пользователей
     * [PLAN] Кэшировать результат для производительности
     * @returns {Array} Массив доступных гексов
     */
    getAvailableHexes() {
        const hexes = this.hexGrid.getAllHexes();
        
        // [ЧТО] Подсчитываем пользователей на каждом гексе
        // [ЗАЧЕМ] Определение заполненности гексов
        // [PLAN] Использовать Map для быстрого подсчёта
        const hexUserCount = {};
        hexes.forEach(hex => {
            hexUserCount[hex.id] = 0;
        });
        
        this.users.forEach(user => {
            if (hexUserCount.hasOwnProperty(user.hexId)) {
                hexUserCount[user.hexId]++;
            }
        });
        
        // [ЧТО] Возвращаем гексы где есть свободные места
        // [ЗАЧЕМ] Только подходящие гексы для размещения
        // [PLAN] Сортировать по заполненности для равномерного распределения
        return hexes.filter(hex => hexUserCount[hex.id] < this.maxUsersPerHex);
    }
    
    /**
     * Генерация случайного цвета для пользователя
     * [ЧТО] Создаёт случайный HEX-цвет
     * [ЗАЧЕМ] Визуальное различие пользователей
     * [PLAN] Использовать предопределённые палитры
     * @returns {string} HEX-код цвета
     */
    generateRandomColor() {
        const colors = [
            '#ff6b6b', '#4ecdc4', '#45b7d1', '#96ceb4',
            '#ffeaa7', '#dfe6e9', '#fd79a8', '#a29bfe',
            '#6c5ce7', '#00b894', '#e17055', '#74b9ff'
        ];
        return colors[Math.floor(Math.random() * colors.length)];
    }
    
    /**
     * Обновление состояния пользователей (перемещение)
     * [ЧТО] Каждую секунду пользователи могут случайно переместиться
     * [ЗАЧЕМ] Симуляция активности на карте
     * [PLAN] Добавить логику принятия решений о перемещении
     */
    update() {
        const now = Date.now();
        
        // [ЧТО] Проверяем прошла ли секунда с последнего обновления
        // [ЗАЧЕМ] Обновление не чаще чем раз в секунду
        // [PLAN] Использовать дельта-время для точности
        if (now - this.lastUpdateTime < this.updateInterval) {
            return;
        }
        
        this.lastUpdateTime = now;
        
        // [ЧТО] Обновляем каждого пользователя
        // [ЗАЧЕМ] Перемещение пользователей по карте
        // [PLAN] Параллельная обработка для производительности
        this.users.forEach(user => {
            this.updateUser(user);
        });
        
        // [ЧТО] Логируем статистику
        // [ЗАЧЕМ] Отладка и мониторинг
        // [PLAN] Отправить в систему статистики
        if (this.users.length > 0 && this.lastUpdateTime % 5000 < 1000) {
            console.log(`[UserManager] Обновлено ${this.users.length} пользователей`);
        }
    }
    
    /**
     * Обновление одного пользователя
     * [ЧТО] Решает переместиться пользователю или остаться
     * [ЗАЧЕМ] Индивидуальная логика перемещения
     * [PLAN] Добавить факторы влияния на решение
     * @param {Object} user - Объект пользователя
     */
    updateUser(user) {
        // [ЧТО] 50% шанс перемещения
        // [ЗАЧЕМ] Случайное поведение - некоторые остаются на месте
        // [PLAN] Настроить вероятность через параметры
        const shouldMove = Math.random() < 0.5;
        
        if (!shouldMove) {
            return; // Остаётся на текущем гексе
        }
        
        // [ЧТО] Получаем текущий гекс пользователя
        // [ЗАЧЕМ] Нужно для определения возможных направлений
        // [PLAN] Проверить валидность гекса
        const currentHex = this.hexGrid.getHexById(user.hexId);
        
        if (!currentHex) {
            console.warn(`[UserManager] Пользователь ${user.id} на несуществующем гексе ${user.hexId}`);
            return;
        }
        
        // [ЧТО] Получаем всех соседей текущего гекса
        // [ЗАЧЕМ] Возможные направления для перемещения
        // [PLAN] Учесть тип местности и препятствия
        const neighbors = currentHex.neighbors;
        const neighborIds = Object.keys(neighbors).map(key => neighbors[key].id);
        
        if (neighborIds.length === 0) {
            return; // Нет куда двигаться (изолированный гекс)
        }
        
        // [ЧТО] Фильтруем соседей где есть место (< 6 пользователей)
        // [ЗАЧЕМ] Нельзя переместиться на заполненный гекс
        // [PLAN] Добавить приоритеты для разных гексов
        const availableNeighbors = neighborIds.filter(neighborId => {
            const countOnHex = this.users.filter(u => u.hexId === neighborId).length;
            return countOnHex < this.maxUsersPerHex;
        });
        
        if (availableNeighbors.length === 0) {
            return; // Все соседи заполнены
        }
        
        // [ЧТО] Выбираем случайного соседа из доступных
        // [ЗАЧЕМ] Случайное перемещение
        // [PLAN] Умный выбор (ближайший к цели, менее заполненный и т.д.)
        const newHexId = availableNeighbors[Math.floor(Math.random() * availableNeighbors.length)];
        
        // [ЧТО] Перемещаем пользователя на новый гекс
        // [ЗАЧЕМ] Обновление позиции
        // [PLAN] Добавить анимацию перемещения
        user.hexId = newHexId;
    }
    
    /**
     * Получение пользователей на конкретном гексе
     * [ЧТО] Фильтрует пользователей по ID гекса
     * [ЗАЧЕМ] Узнать кто находится на гексе
     * [PLAN] Использовать spatial hash для быстрого поиска
     * @param {number} hexId - ID гекса
     * @returns {Array} Массив пользователей на гексе
     */
    getUsersOnHex(hexId) {
        return this.users.filter(user => user.hexId === hexId);
    }
    
    /**
     * Получение количества пользователей на гексе
     * [ЧТО] Подсчитывает пользователей на гексе
     * [ЗАЧЕМ] Быстрая проверка заполненности
     * [PLAN] Кэшировать результаты
     * @param {number} hexId - ID гекса
     * @returns {number} Количество пользователей
     */
    getUserCountOnHex(hexId) {
        return this.users.filter(user => user.hexId === hexId).length;
    }
    
    /**
     * Получение всех пользователей
     * [ЧТО] Возвращает ссылку на массив пользователей
     * [ЗАЧЕМ] Для отрисовки и других операций
     * [PLAN] Вернуть копию для безопасности
     * @returns {Array} Массив всех пользователей
     */
    getAllUsers() {
        return this.users;
    }
    
    /**
     * Получение общего количества пользователей
     * [ЧТО] Возвращает длину массива пользователей
     * [ЗАЧЕМ] Статистика и отладка
     * [PLAN] Добавить детальную статистику
     * @returns {number} Количество пользователей
     */
    getTotalUserCount() {
        return this.users.length;
    }
    
    /**
     * Удаление пользователя
     * [ЧТО] Удаляет пользователя из массива
     * [ЗАЧЕМ] Освобождение места, механики игры
     * [PLAN] Добавить события при удалении
     * @param {number} userId - ID пользователя для удаления
     * @returns {boolean} true если успешно удалён
     */
    removeUser(userId) {
        const index = this.users.findIndex(user => user.id === userId);
        
        if (index === -1) {
            return false; // Пользователь не найден
        }
        
        this.users.splice(index, 1);
        console.log(`[UserManager] Пользователь ${userId} удалён`);
        return true;
    }
    
    /**
     * Очистка всех пользователей
     * [ЧТО] Удаляет всех пользователей с карты
     * [ЗАЧЕМ] Сброс состояния, перезапуск
     * [PLAN] Добавить событие очистки
     */
    clearAllUsers() {
        this.users = [];
        console.log('[UserManager] Все пользователи удалены');
    }
    
    /**
     * Заполнение карты начальными пользователями
     * [ЧТО] Создаёт указанное количество пользователей
     * [ЗАЧЕМ] Начальное состояние игры
     * [PLAN] Умное размещение (равномерно, кластерами и т.д.)
     * @param {number} count - Количество пользователей для создания
     */
    populateInitialUsers(count) {
        console.log(`[UserManager] Создаём ${count} начальных пользователей...`);
        
        let created = 0;
        for (let i = 0; i < count; i++) {
            const user = this.createUser();
            if (user) {
                created++;
            } else {
                break; // Достигнут лимит
            }
        }
        
        console.log(`[UserManager] Создано ${created} пользователей из ${count} запланированных`);
    }
}

